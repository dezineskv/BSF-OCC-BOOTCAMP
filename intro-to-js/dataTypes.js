// primitive data types use typeof to identify it
// numbers 0-9
let score = 18;
console.log("Type of score", typeof score);

// strings
let playerName = 'Kim_123';
console.log("Type of playerName", typeof playerName);
// if number is within quotes, it's a string

// boolean
let isAdmin = true;
isAdmin = false;
console.log('Type of', typeof isAdmin);

// null. represents intentional absence of value. type is object.
let car = null;
console.log('Value of', car);
console.log('Type of', typeof car);

// undefined. represents unintentional absence of value. 
let city;
console.log("Value of: ", city);
console.log("Type of: ", typeof city);
